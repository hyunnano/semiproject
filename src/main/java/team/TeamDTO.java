package team;

public class TeamDTO {
	
	private String id; //아이디
	private String t_num; //팀번호
	private String t_name; //팀이름
	private String t_age; //팀연령대
	private String t_type; //팀경기유형
	private String t_local; //팀활동지역
	private String t_level; //팀실력
	private String t_info; //팀정보
	private String grade; //등급(권한)
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getT_num() {
		return t_num;
	}
	public void setT_num(String t_num) {
		this.t_num = t_num;
	}
	public String getT_name() {
		return t_name;
	}
	public void setT_name(String t_name) {
		this.t_name = t_name;
	}
	public String getT_age() {
		return t_age;
	}
	public void setT_age(String t_age) {
		this.t_age = t_age;
	}
	public String getT_type() {
		return t_type;
	}
	public void setT_type(String t_type) {
		this.t_type = t_type;
	}
	public String getT_local() {
		return t_local;
	}
	public void setT_local(String t_local) {
		this.t_local = t_local;
	}
	public String getT_level() {
		return t_level;
	}
	public void setT_level(String t_level) {
		this.t_level = t_level;
	}
	public String getT_info() {
		return t_info;
	}
	public void setT_info(String t_info) {
		this.t_info = t_info;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	
	

}
